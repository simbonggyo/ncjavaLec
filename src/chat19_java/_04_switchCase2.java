package chat19_java;

public class _04_switchCase2 {

	public static void main(String[] args) {
		
		//break문을 넣어야 정상적으로 동작하기 때문에
		//case별로 항상 break문을 넣어줘야 된다.
		//break문이 없는 경우에는 맞는 조건부터 break문을
		//만날때까지는 case가 모두 실행된다.
		switch(1) {
		case 1:
			System.out.println("");
			break;
		case 2:
			System.out.println("");
			break;
	
	}

}
}
